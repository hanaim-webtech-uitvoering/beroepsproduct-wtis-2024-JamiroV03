<h2>Privacyverklaring</h2>

<p>Deze privacyverklaring is van toepassing op de webapplicatie van Pizzeria Sole Machina.</p>

<h3>1. Welke gegevens verzamelen wij?</h3>
<p>Bij het registreren en gebruiken van deze webapp verzamelen wij de volgende persoonsgegevens:</p>
<ul>
    <li>Voor- en achternaam</li>
    <li>Gebruikersnaam</li>
    <li>Adres voor bezorging</li>
    <li>Bestelgegevens (producten, tijd, status)</li>
</ul>

<h3>2. Waarom verzamelen wij deze gegevens?</h3>
<p>Wij verwerken je gegevens uitsluitend om:</p>
<ul>
    <li>Je bestellingen correct te verwerken en bezorgen</li>
    <li>Je gebruikersaccount te beheren</li>
    <li>Personeel inzicht te geven in actieve bestellingen</li>
</ul>

<h3>3. Wie heeft toegang tot jouw gegevens?</h3>
<p>Alleen medewerkers van Pizzeria Sole Machina (met een personeelsaccount) hebben toegang tot de bestellingen die zij moeten voorbereiden of bezorgen.</p>

<h3>4. Hoe lang bewaren wij gegevens?</h3>
<p>Gegevens worden bewaard zolang dat nodig is voor het uitvoeren van bestellingen. Accounts en bestelhistorie blijven beschikbaar totdat je jouw account verwijdert of hierom vraagt.</p>

<h3>5. Gegevens inzien of laten verwijderen</h3>
<p>Wil je jouw persoonsgegevens inzien, aanpassen of laten verwijderen? Neem dan contact op via onze contactpagina of stuur een verzoek naar het personeel.</p>

<h3>6. Beveiliging</h3>
<p>Je wachtwoord wordt veilig opgeslagen via hashing. Ingevoerde gegevens worden beschermd tegen ongewenste toegang. Alleen bevoegde gebruikers kunnen inloggen.</p>

